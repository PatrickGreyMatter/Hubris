        <h1>À propos de Hubris</h1>
        <p>Bienvenue sur Hubris, une plateforme dédiée à la célébration et à la redécouverte des films anciens, cultes et libres de droits. Fondé par Patrick.M, étudiant en BTS SIO à L'ICFA de l'Enclos Saint-François de Montpellier dans le cadre d'un projet étudiant, Hubris vise à offrir une nouvelle vie à des œuvres cinématographiques qui, malgré leur importance historique et culturelle, ne sont pas facilement accessibles au grand public.</p>
        
        <h2>Notre Mission</h2>
        <p>La mission de Hubris est de créer un espace où la passion pour le cinéma classique et indépendant est au premier plan. Nous cherchons à fournir un accès facile à une vaste bibliothèque de films qui, autrement, resteraient dans l'ombre des nouveautés commerciales. Chaque film sur Hubris est accompagné de discussions riches et de commentaires de notre communauté engagée, permettant une compréhension plus profonde et une appréciation accrue des œuvres présentées.</p>

        <h2>Caractéristiques de la Plateforme</h2>
        <p>Hubris n'est pas seulement un site de streaming ; c'est une communauté. Voici quelques-unes des caractéristiques qui nous distinguent :</p>
        <ul>
            <li><strong>Liberté d'échange :</strong> Les utilisateurs peuvent commenter les films, partager leurs pensées et discuter des thèmes et des détails cinématographiques avec d'autres amateurs de cinéma.</li>
            <li><strong>Diversité des rôles :</strong> Notre site accueille des spectateurs, des contributeurs qui peuvent soumettre leurs propres films, ainsi que des administrateurs qui veillent à la qualité et à la pertinence des contenus proposés.</li>
            <li><strong>Tags et Catégorisation :</strong> Les films sont organisés par tags et catégories, permettant aux utilisateurs de naviguer facilement dans notre bibliothèque et de trouver des films selon leur humeur ou leurs intérêts spécifiques.</li>
            <li><strong>Accessibilité :</strong> Hubris est conçu pour être accessible à tous, avec une interface facile à utiliser, peu importe votre niveau de compétence technique.</li>
        </ul>

        <h2>Engagement envers la Communauté</h2>
        <p>Nous croyons fermement à l'importance de construire une communauté autour du cinéma. Hubris offre donc des outils pour faciliter les interactions et encourager les discussions enrichissantes. Nous nous engageons à maintenir une plateforme sécurisée, respectueuse et ouverte à toutes et à tous.</p>

        <h2>Rejoignez-nous</h2>
        <p>Que vous soyez un cinéphile aguerri, un étudiant en cinéma, ou simplement curieux de découvrir des films hors du commun, Hubris vous offre une porte d'entrée dans un monde de découvertes cinématographiques. Inscrivez-vous aujourd'hui pour explorer, discuter et participer à la renaissance du cinéma classique et libre de droits.</p>

